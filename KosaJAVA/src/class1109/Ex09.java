package class1109;

public class Ex09 {
    private String kind;
    private String color;
    private String name;

    public Ex09() {
    }

    public Ex09(String kind, String color, String name) {
        this.kind = kind;
        this.color = color;
        this.name = name;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
